#include "3-calc.h"
/**
 * op_add - adds two int
 * @a: first int
 * @b: sec int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two ints
 * @a: first
 * @b: sec
 * Return: res
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul two ints
 * @a: first int
 * @b: second int
 * Return: res
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div two nums
 * @a: first num
 * @b: second
 * Return: a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a mod b
 * @a: first
 * @b: sec
 * Return: a%b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
